// Codes journée entière → écrits sur M+AM+N
const JOURNEE_ENTIERE = ['CA', 'CF', 'ADM', 'X', 'RC', 'DEB', 'FIN'];

export async function GET(request) {
  const { searchParams } = new URL(request.url);
  if (searchParams.get('secret') !== SECRET) {
    return NextResponse.json({ error: 'Non autorisé' }, { status: 401 });
  }

  const data = await kv.get(KEY);
  if (!data) return NextResponse.json({ error: 'Planning vide' }, { status: 404 });

  const cat = data.medical;
  if (!cat) return NextResponse.json({ error: 'Catégorie medical introuvable' }, { status: 404 });

  const agents = cat.agents || [];
  const cellules = { ...cat.cellules };
  const log = [];
  let cellsWritten = 0;
  const notFound = [];

  for (const [nom, jours] of Object.entries(PLANNING_DATA)) {
    // Chercher l'agent par nom de famille (insensible à la casse)
    const agent = agents.find(a =>
      a.nomFamille && a.nomFamille.toUpperCase().includes(nom.toUpperCase().split(' ')[0])
    );
    if (!agent) {
      notFound.push(nom);
      continue;
    }

    for (const [date, codes] of Object.entries(jours)) {
      // Effacer les 3 créneaux d'abord
      delete cellules[`${agent.id}|${date}|M`];
      delete cellules[`${agent.id}|${date}|AM`];
      delete cellules[`${agent.id}|${date}|N`];

      // Écrire les nouveaux codes
      for (const [moment, code] of Object.entries(codes)) {
        if (JOURNEE_ENTIERE.includes(code)) {
          // Journée entière → écrire sur M+AM+N
          ['M', 'AM', 'N'].forEach(m => {
            cellules[`${agent.id}|${date}|${m}`] = code;
            cellsWritten++;
          });
          break; // Un seul code journée entière suffit
        } else {
          cellules[`${agent.id}|${date}|${moment}`] = code;
          cellsWritten++;
        }
      }
    }

    log.push(`${nom} (${agent.id}) → ${Object.keys(jours).length} jours importés`);
  }

  data.medical = { ...cat, cellules };
  await kv.set(KEY, data);

  return NextResponse.json({
    ok: true,
    cellsWritten,
    agentsImported: log.length,
    log,
    notFound,
  });
}
